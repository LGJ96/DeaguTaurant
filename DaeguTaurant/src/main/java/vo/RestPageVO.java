package vo;

public class RestPageVO {
	private int rest_number;
	private int rest_count;
	private int rest_currentPage;
	private int rest_pageCount;
	private int rest_startPage;
	private int rest_endPage;
	
	
	public int getRest_number() {
		return rest_number;
	}
	public void setRest_number(int rest_number) {
		this.rest_number = rest_number;
	}
	public int getRest_count() {
		return rest_count;
	}
	public void setRest_count(int rest_count) {
		this.rest_count = rest_count;
	}
	public int getRest_currentPage() {
		return rest_currentPage;
	}
	public void setRest_currentPage(int rest_currentPage) {
		this.rest_currentPage = rest_currentPage;
	}
	public int getRest_pageCount() {
		return rest_pageCount;
	}
	public void setRest_pageCount(int rest_pageCount) {
		this.rest_pageCount = rest_pageCount;
	}
	public int getRest_startPage() {
		return rest_startPage;
	}
	public void setRest_startPage(int rest_startPage) {
		this.rest_startPage = rest_startPage;
	}
	public int getRest_endPage() {
		return rest_endPage;
	}
	public void setRest_endPage(int rest_endPage) {
		this.rest_endPage = rest_endPage;
	}
	
	
	
	
	
}
