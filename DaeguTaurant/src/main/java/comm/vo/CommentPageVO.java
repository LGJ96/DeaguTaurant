package comm.vo;

public class CommentPageVO {

	private int comment_number;
	private int comment_count;
	private int comment_currentPage;
	private int comment_pageCount;
	private int comment_startPage;
	private int comment_endPage;
	
	public int getComment_number() {
		return comment_number;
	}
	public void setComment_number(int comment_number) {
		this.comment_number = comment_number;
	}
	public int getComment_count() {
		return comment_count;
	}
	public void setComment_count(int comment_count) {
		this.comment_count = comment_count;
	}
	public int getComment_currentPage() {
		return comment_currentPage;
	}
	public void setComment_currentPage(int comment_currentPage) {
		this.comment_currentPage = comment_currentPage;
	}
	public int getComment_pageCount() {
		return comment_pageCount;
	}
	public void setComment_pageCount(int comment_pageCount) {
		this.comment_pageCount = comment_pageCount;
	}
	public int getComment_startPage() {
		return comment_startPage;
	}
	public void setComment_startPage(int comment_startPage) {
		this.comment_startPage = comment_startPage;
	}
	public int getComment_endPage() {
		return comment_endPage;
	}
	public void setComment_endPage(int comment_endPage) {
		this.comment_endPage = comment_endPage;
	}
	
	
}
