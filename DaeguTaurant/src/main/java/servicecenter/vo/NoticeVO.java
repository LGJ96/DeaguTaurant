package servicecenter.vo;

import java.sql.Timestamp;

public class NoticeVO {
	private int cus_notice_number;
	private String cus_notice_title;
	private Timestamp cus_notice_date;
	private int cus_notice_count;
	private String cus_notice_content;
	private int cus_notice_list_number;
	
	public int getCus_notice_number() {
		return cus_notice_number;
	}
	public void setCus_notice_number(int cus_notice_number) {
		this.cus_notice_number = cus_notice_number;
	}
	public String getCus_notice_title() {
		return cus_notice_title;
	}
	public void setCus_notice_title(String cus_notice_title) {
		this.cus_notice_title = cus_notice_title;
	}
	public Timestamp getCus_notice_date() {
		return cus_notice_date;
	}
	public void setCus_notice_date(Timestamp cus_notice_date) {
		this.cus_notice_date = cus_notice_date;
	}
	public int getCus_notice_count() {
		return cus_notice_count;
	}
	public void setCus_notice_count(int cus_notice_count) {
		this.cus_notice_count = cus_notice_count;
	}
	public String getCus_notice_content() {
		return cus_notice_content;
	}
	public void setCus_notice_content(String cus_notice_content) {
		this.cus_notice_content = cus_notice_content;
	}
	public int getCus_notice_list_number() {
		return cus_notice_list_number;
	}
	public void setCus_notice_list_number(int cus_notice_list_number) {
		this.cus_notice_list_number = cus_notice_list_number;
	}
	
	
	
	
}
